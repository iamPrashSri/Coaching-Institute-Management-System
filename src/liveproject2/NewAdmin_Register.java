    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fname=jTextField1.getText();
        String mname=jTextField2.getText();
        String lname=jTextField3.getText();
        String email_id=jTextField4.getText();
        String admin_id=jTextField5.getText();
        String username=jTextField6.getText();
        String password=jPasswordField1.getText();
        String cpassword=jPasswordField2.getText();
        if(password.equalsIgnoreCase(cpassword))
        {
            try {
                int val=DbConnection.insert_into_admin_login(admin_id,fname,mname,lname,email_id,username,password);
                if(val>0)
                {
       JOptionPane.showMessageDialog(this,"You have successfully registered as Administrator","Administrator Registration",JOptionPane.PLAIN_MESSAGE);
       jTextField1.setText("");
       jTextField2.setText("");
       jTextField3.setText("");
       jTextField4.setText("");
       jTextField5.setText("");
       jTextField6.setText("");
       jPasswordField1.setText("");
       jPasswordField2.setText("");
       this.setVisible(false);
       new AdminLogin().setVisible(true);
                }
                else
                {
       JOptionPane.showMessageDialog(this,"Sorry...Some problem has occured...Please try again","Administrator Registration",JOptionPane.ERROR_MESSAGE);             
                }
            } catch (Exception ex) {
                Logger.getLogger(NewAdmin_Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Sorry...passwords do not match!!!","Administrator Registration",JOptionPane.ERROR_MESSAGE);
            jPasswordField1.setText("");
            jPasswordField2.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

